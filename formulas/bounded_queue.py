"""Многоканальная СМО с ограниченной очередью"""

import math

def calculate_smo(n: int, m: int, lmbda: float, mu: float) -> dict:
    """
    Расчет характеристик многоканальной СМО с ограниченной очередью.
    
    :param n: Количество каналов обслуживания (n > 0)
    :param m: Максимальное число мест в очереди (m > 0)
    :param lmbda: Интенсивность входящего потока заявок (лямбда)
    :param mu: Интенсивность обслуживания одного канала (мю)
    :return: Словарь с рассчитанными показателями эффективности
    """
    # Основные промежуточные параметры
    rho = lmbda / mu
    rho_n = rho / n
    t_ob = 1 / mu

    # 1. Расчет вероятности p0 (система свободна)
    # Сумма для состояний, когда заняты от 0 до n каналов
    sum_channels = sum((rho ** k) / math.factorial(k) for k in range(n + 1))
    
    # Специфическое слагаемое для очереди (зависит от того, равен ли rho/n единице)
    if abs(rho_n - 1.0) < 1e-9:  # Случай 2: rho/n == 1
        sum_queue = m * (rho ** n) / math.factorial(n)
        is_critical_case = True
    else:                        # Случай 1: rho/n != 1
        sum_queue = (rho**(n+1)*(1-(rho/n)**m)) / (math.factorial(n) * n * (1 - rho/n))
        is_critical_case = False

    p0 = 1 / (sum_channels + sum_queue)

    # 2. Вероятности состояний и показатели отказа/пропускной способности
    p_n = (rho ** n) / math.factorial(n) * p0
    
    if is_critical_case:
        p_otk = p_n
        p_och = m * p_n
        l_och = (m * (m + 1) / 2) * p_n
    else:
        p_otk = p_n * (rho_n ** m)
        p_och = p_n * rho_n * ((1 - rho_n ** m) / (1 - rho_n))
        l_och = (p_n * rho_n / (1 - rho_n) ** 2) * (1 - (rho_n ** m) * (1 + m * (1 - rho_n)))

    q = 1 - p_otk
    a = lmbda * q

    # 3. Среднее число заявок в разных состояниях
    l_ob = a / mu
    l_sist = l_ob + l_och

    # 4. Временные показатели (Формулы Литтла)
    t_och = l_och / lmbda
    t_ob_fact = q / mu
    t_sist = l_sist / lmbda
    
    # Коэффициент загрузки каналов
    k_zag = l_ob / n

    # Собираем результаты в словарь
    results = {
        "Режим работы СМО": "rho/n = 1 (критический)" if is_critical_case else "rho/n != 1",
        "Параметр загрузки (rho)": rho,
        "Отношение rho/n": rho_n,
        "Вероятность p0 (система свободна)": p0,
        "Вероятность отказа (Pотк)": p_otk,
        "Относительная пропускная способность (Q)": q,
        "Абсолютная пропускная способность (A)": a,
        "Вероятность наличия очереди (Pоч)": p_och,
        "Среднее число занятых каналов (Lоб)": l_ob,
        "Среднее число заявок в очереди (Lоч)": l_och,
        "Среднее число заявок в системе (Lсист)": l_sist,
        "Среднее время в очереди (Tоч)": t_och,
        "Среднее время под обслуживанием (Tоб)": t_ob_fact,
        "Среднее время в системе (Tсист)": t_sist,
        "Коэффициент загрузки каналов (kзаг)": k_zag
    }

    return results